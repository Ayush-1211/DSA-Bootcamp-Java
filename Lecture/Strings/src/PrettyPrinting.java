public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 453.1274f;
        System.out.printf("Formatted number is %.2f", a);   // printf = formatted print
        System.out.println();

        System.out.printf("PI %.3f", Math.PI);
        System.out.println();

        System.out.printf("Hello my name is %s and I am %s", "Ayush", "Cool");
    }
}
